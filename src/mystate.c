void dumpHex(const unsigned char *package, unsigned int len, int isReceivedPackage) {
#ifdef DEBUG
	printf("---------------%s--------------------\n", 
			isReceivedPackage ? 
						"Receive" 
					 :  "--Send-");
	printf("0x000 ");
	for (int j = 0; j < 16; j++) {
		printf("0x%02X  ", j);
		if (j == 7) printf(" ");
	}
	puts("");
	for (int i = 0; i < len; ) {
		printf("0x%03x ", i);
		for (int j = 0; j < 16 && i < len; j++) {
			printf("0x%02X  ", (int)package[i]);
			if (j == 7) printf(" ");
			++i;
		}
		puts("");
	}
	puts("===========================================");
#endif // DEBUG
}


